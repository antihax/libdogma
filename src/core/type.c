#include "tables.h"

uint8_t dogma_get_category_for_typeid(dogma_typeid_t tid) {
	const dogma_type_t* t;
	int r = dogma_get_type(tid, &t);
	if (r != DOGMA_OK) {
		return 0;
	}

	return t->categoryid;
}
